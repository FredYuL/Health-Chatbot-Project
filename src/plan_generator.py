def generate_plan(goal):
    if goal == '减肥':
        plan = "您的减肥计划：\n1. 每日摄入热量控制在1500千卡以内。\n2. 每周进行至少3次有氧运动，每次30分钟。\n3. 多吃蔬菜和水果，减少高脂肪食物摄入。"
    elif goal == '增肌':
        plan = "您的增肌计划：\n1. 增加蛋白质摄入，如鸡胸肉、蛋白粉等。\n2. 每周进行4次力量训练，每次45分钟。\n3. 保证充足的睡眠和休息。"
    else:
        plan = "抱歉，暂时无法为您生成该计划。"
    return plan
